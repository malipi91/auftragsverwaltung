    private boolean istGueltigesDatum(String datum) {
        boolean istGueltig = false;
        SimpleDateFormat df = new SimpleDateFormat("dd.MM.yyyy");
        df.setLenient(false);
        try {
            Date date = df.parse(datum);
            istGueltig = true;
        } catch (ParseException ex) {
        }
        return istGueltig;
    }
